import 'package:flutter/material.dart';

class Purchase {
  final String product;
  final int quantity;
  final String buyer;
  final String id;
  final String seller;
  final String day;
  final String month;
  final String year;






  Purchase({
    @required this.product,
    @required this.quantity,
    @required this.buyer,
    @required this.id,
    @required this.seller,
    @required this.day,
    @required this.month,
    @required this.year,

  });
}
